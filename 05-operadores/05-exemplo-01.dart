void main() {
  int a = 10;
  print('a = $a');

  int b = 3;
  print('b = $b');

  int soma = a + b;
  print('a + b = $soma');

  int subtracao = a - b;
  print('a - b = $subtracao');

  int multiplicacao = a * b;
  print('a * b = $multiplicacao');

  double divisao = a / b;
  print('divisão a/b = $divisao');

  int resto = a % b;
  print('resto a % b  = $resto');

  int incremento = a++;
  print('pós incremento a++ = $incremento');

  int decremento = a--;
  print('pós decremento a-- = $decremento');
  print('a = $a');
}
